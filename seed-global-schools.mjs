async function seed() {
  const conn = await createConnection(DB_URL);
  console.log(`Seeding ${newSchools.length} new global schools...`);

  let inserted = 0;
  let skipped = 0;

  for (const school of newSchools) {
    // Check if school already exists (by name)
    const [existing] = await conn.execute(
      "SELECT id FROM flight_schools WHERE name = ?",
      [school.name]
    );
    if (existing.length > 0) {
      console.log(`  SKIP (exists): ${school.name}`);
      skipped++;
      continue;
    }

    await conn.execute(
      `INSERT INTO flight_schools
        (name, country, city, airport, integratedAtpl, modularAtpl, ppl,
         priceRange, financeAvailable, accommodationAvailable,
         airlinePartnerships, website, description)
       VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)`,
      [
        school.name,
        school.country,
        school.city,
        school.airport,
        school.integratedAtpl ? 1 : 0,
        school.modularAtpl ? 1 : 0,
        school.ppl ? 1 : 0,
        school.priceRange,
        school.financeAvailable,
        school.accommodationAvailable,
        school.airlinePartnerships,
        school.website,
        school.description,
      ]
    );
    console.log(`  INSERTED: ${school.name} (${school.country})`);
    inserted++;
  }

  await conn.end();
  console.log(`\nDone. Inserted: ${inserted}, Skipped: ${skipped}`);

  // Print final country breakdown
  const conn2 = await createConnection(DB_URL);
  const [rows] = await conn2.execute(
    "SELECT country, COUNT(*) as count FROM flight_schools GROUP BY country ORDER BY count DESC"
  );
  console.log("\nFinal school count by country:");
  for (const row of rows) {
    console.log(`  ${row.country}: ${row.count}`);
  }
  await conn2.end();
}

seed().catch(console.error);
